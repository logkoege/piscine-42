int	ft_str_is_lowercase(char *str)
{
	int	i;

	i = 0;
	while ((str[i] > 96 && str[i] < 123))
	{
		i++;
	}
	if (str[i] == '\0')
	{
		return (1);
	}
	return (0);
}
